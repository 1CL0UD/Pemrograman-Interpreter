class embedClass:        

    def setContent(self, content):
        self.content = content
    def getContent(self):
        return self.content

    def setColor(self, color):
        self.color = color
    def getColor(self):
        return self.color
    
    def setAuthorName(self, authorName):
        self.authorName = authorName
    def getAuthorName(self):
        return self.authorName
    
    def setTitle(self, title):
        self.title = title
    def getTitle(self):
        return self.title

    def setUrl(self, url):
        self.url = url
    def getUrl(self):
        return self.url
    
    def setDesc(self, description):
        self.description = description
    def getDesc(self):
        return self.description